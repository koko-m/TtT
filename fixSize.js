// fix the size of the canvas and the textarea

var areaWidth = window.innerWidth;
var areaHeight = window.innerHeight - 220;

function getCanvasWidth () { return areaWidth * 3 / 4; }
function getCanvasHeight () { return areaHeight; }
function getLogAreaWidth () { return areaWidth / 5; }
function getLogAreaHeight () { return areaHeight; }
